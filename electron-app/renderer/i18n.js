function detectLanguage() {
  var saved = localStorage.getItem('autoto_lang');
  if (saved) return saved;
  var browserLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
  if (browserLang.startsWith('zh-tw') || browserLang === 'zh-hant') return 'zh-TW';
  if (browserLang.startsWith('zh')) return 'zh-CN';
  if (browserLang.startsWith('ja')) return 'ja';
  if (browserLang.startsWith('ko')) return 'ko';
  return 'en';
}

let currentLang = detectLanguage();

function t(key, vars) {
  const lang = I18N[currentLang] || I18N['zh-TW'];
  let text = lang[key] || I18N['zh-TW'][key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('autoto_lang', lang);
  applyLanguage();
  // 重新渲染動態內容
  if (typeof showWelcome === 'function' && document.querySelector('.welcome-msg')) {
    showWelcome();
  }
  if (typeof loadSkills === 'function' && currentPage === 'skills') {
    loadSkills();
  }
}

function applyLanguage() {
  // 側欄
  document.querySelectorAll('.nav-item').forEach(btn => {
    const page = btn.dataset.page;
    const label = btn.querySelector('.nav-label');
    if (label && page) {
      label.textContent = t('nav_' + page);
      btn.title = t('nav_' + page);
    }
  });
  // 設定 tabs
  document.querySelectorAll('.settings-tab').forEach(tab => {
    const stab = tab.dataset.stab;
    if (stab) tab.textContent = t('stab_' + stab);
  });
  // data-i18n 屬性
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.innerHTML = t(key);
    }
  });
  // data-i18n-title
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // 更新 HTML lang 屬性
  document.documentElement.lang = currentLang === 'zh-TW' ? 'zh-TW' : currentLang === 'zh-CN' ? 'zh-CN' : currentLang;
}
